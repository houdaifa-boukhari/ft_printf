#include "ft_printf.h"

int	putnbr_(long nb)
{
	int	i;

	i = 1;
	if (nb < 0)
	{
		nb *= -1;
		i += putchar_('-');
	}
	if (nb >= 10)
	{
		i += putnbr_(nb / 10);
		putnbr_(nb % 10);
	}
	else
		putchar_(nb + 48);
	return (i);
}
